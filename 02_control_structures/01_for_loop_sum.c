//用迴圈來呈現0_sum.c，可以讓整個程式碼更有彈性，不被局限只能輸入兩個數字
/*
for (初始化; 條件判斷; 更新) {
    // 迴圈內部要執行的程式
}
*/
#include <stdio.h>

int main() {
    int n, num, sum = 0;

    printf("請輸入要計算的數字個數: ");
    scanf("%d", &n);  // 讓使用者輸入要計算的數字個數

    for (int i = 1; i <= n; i++) {
        printf("請輸入第 %d 個整數: ", i);
        scanf("%d", &num);
        sum += num;  // 把輸入的數字加到總和中
    }

    printf("總和是: %d\n", sum);  // 輸出結果
    return 0;
}

